import numpy as np

class LogisticRegression(object):

    def __init__(self, x, Y, alpha=0.05, lam=0.1):

        self.xMean = np.mean(x, axis=0)
        self.xStd = np.std(x, axis=0)
        x = (x - self.xMean) / self.xStd

        const = np.array([1] * x.shape[0]).reshape(x.shape[0], 1)
        self.theta = np.array([0.0] * (x.shape[1] + 1))

        self.X = np.append(const, x, axis=1)
        self.Y = Y
        self.alpha = alpha
        self.lam = lam


    @staticmethod
    def _sigmoid(x):
        return 1.0 / (1.0 + np.exp((-1) * x))


    def _gradientDescend(self, iters):

        m, n = self.X.shape

        for i in range(0, iters):
            theta_temp = self.theta

        h_theta = self._sigmoid(np.dot(self.X, self.theta))
        diff = h_theta - self.Y
        self.theta[0] = theta_temp[0] - self.alpha * \
            (1.0 / m) * sum(diff * self.X[:, 0])

        for j in range(1, n):
            val = theta_temp[j] - self.alpha * (1.0 / m) * (sum(diff * self.X[:, j]) + self.lam * m * theta_temp[j])
            self.theta[j] = val


    # prediction
    def predict(self, X):

        x = (X - self.xMean) / self.xStd

        const = np.array([1] * X.shape[0]).reshape(X.shape[0], 1)
        X = np.append(const, x, axis=1)
        pred = self._sigmoid(np.dot(X, self.theta))

        np.putmask(pred, pred >= 0.5, 1.0)
        np.putmask(pred, pred < 0.5, 0.0)

        return pred


